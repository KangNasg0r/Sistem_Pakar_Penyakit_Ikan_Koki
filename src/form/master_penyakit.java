    protected void autonumber() {
        try {
            String sql = "SELECT kode_penyakit from penyakit order by kode_penyakit asc";
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            kode_penyakit.setText("P01");
            while (rs.next()) {
                String id_penyakit = rs.getString("kode_penyakit").substring(1);
                int AN = Integer.parseInt(id_penyakit) + 1;
                String Nol = "";
                if (AN < 10) {
                    Nol = "0";
                }
                kode_penyakit.setText("P" + Nol + AN);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Nomor Otomatis Gagal" + e);
        }
    }

