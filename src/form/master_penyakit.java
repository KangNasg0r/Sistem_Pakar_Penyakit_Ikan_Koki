    private void bhapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bhapusActionPerformed
        String kodeText = kode_penyakit.getText().trim();

        if (kodeText.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Kode Penyakit belum diisi.", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }

        String cekSql = "SELECT kode_penyakit FROM penyakit WHERE kode_penyakit = ?";

        try {
            PreparedStatement cekStat = conn.prepareStatement(cekSql);
            cekStat.setString(1, kodeText);
            ResultSet cekRs = cekStat.executeQuery();

            if (!cekRs.next()) {
                JOptionPane.showMessageDialog(this, "Kode Penyakit tidak ditemukan.", "Peringatan", JOptionPane.WARNING_MESSAGE);
                return;
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal mengecek data penyakit: " + e);
            return;
        }

        int ok = JOptionPane.showConfirmDialog(
                null,
                "Apakah benar data penyakit ingin dihapus?",
                "Peringatan!",
                JOptionPane.YES_NO_OPTION
        );

        if (ok == JOptionPane.YES_OPTION) {
            String sql = "DELETE FROM penyakit WHERE kode_penyakit = ?";

            try {
                PreparedStatement stat = conn.prepareStatement(sql);
                stat.setString(1, kodeText);
                stat.executeUpdate();

                JOptionPane.showMessageDialog(null, "Data penyakit berhasil dihapus");

                kosong();
                aktif();
                autonumber();
                datatable();

            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "Data penyakit gagal dihapus! " + e);
            }
        }
    }//GEN-LAST:event_bhapusActionPerformed

