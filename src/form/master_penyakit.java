    protected void datatable() {
        Object[] Baris = {"Kode Penyakit", "Nama Penyakit", "Deskripsi", "Solusi"};
        tabmode = new DefaultTableModel(null, Baris);
        String cariitem = txtcari.getText();

        try {
            String sql = "SELECT * FROM penyakit WHERE kode_penyakit LIKE '%" + cariitem + "%' "
                    + "OR nama_penyakit LIKE '%" + cariitem + "%' "
                    + "OR deskripsi LIKE '%" + cariitem + "%' "
                    + "OR solusi LIKE '%" + cariitem + "%' "
                    + "ORDER BY kode_penyakit ASC";
            Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while (hasil.next()) {
                tabmode.addRow(new Object[]{
                    hasil.getString(1),
                    hasil.getString(2),
                    hasil.getString(3),
                    hasil.getString(4),});
            }
            table_penyakit.setModel(tabmode);
            aturTabelPenyakit();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "data gagal dipanggil" + e);
        }
    }

