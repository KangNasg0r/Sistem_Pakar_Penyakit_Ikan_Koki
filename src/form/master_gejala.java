    protected void autonumber() {
        try {
            String sql = "SELECT kode_gejala from gejala order by kode_gejala asc";
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            kode_gejala.setText("G01");
            while (rs.next()) {
                String kd_gejala = rs.getString("kode_gejala").substring(1);
                int AN = Integer.parseInt(kd_gejala) + 1;
                String Nol = "";
                if (AN < 10) {
                    Nol = "0";
                }
                kode_gejala.setText("G" + Nol + AN);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Kode Otomatis Gagal" + e);
        }
    }

    protected void aktif() {
        nama_gejala.requestFocus();
        kode_gejala.setEditable(false);
    }

    protected void kosong() {
        kode_gejala.setText("");
        nama_gejala.setText("");
    }

    protected void datatable() {
        Object[] Baris = {"Kode Gejala", "Nama Gejala"};
        tabmode = new DefaultTableModel(null, Baris);
        String cariitem = txtcari.getText();

        try {
            String sql = "SELECT * FROM gejala WHERE kode_gejala LIKE '%" + cariitem + "%' "
                    + "OR nama_gejala LIKE '%" + cariitem + "%' "
                    + "ORDER BY kode_gejala ASC";

            Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);

            while (hasil.next()) {
                tabmode.addRow(new Object[]{
                    hasil.getString("kode_gejala"),
                    hasil.getString("nama_gejala")
                });
            }

            table_gejala.setModel(tabmode);
            aturTabelGejala();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Data gagal dipanggil: " + e);
        }
    }

