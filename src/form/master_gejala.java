    private void bsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bsimpanActionPerformed

        if (kode_gejala.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Kode gejala belum diisi");
            kode_gejala.requestFocus();
            return;
        }

        if (nama_gejala.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Nama gejala belum diisi");
            nama_gejala.requestFocus();
            return;
        }

        String sql = "INSERT INTO gejala VALUES (?, ?)";

        try {
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setString(1, kode_gejala.getText());
            stat.setString(2, nama_gejala.getText());

            stat.executeUpdate();

            JOptionPane.showMessageDialog(null, "Data gejala berhasil disimpan");
            kosong();
            aktif();
            datatable();
            autonumber();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Data gagal disimpan: " + e);
        }
    }//GEN-LAST:event_bsimpanActionPerformed

