    protected void autonumber() {
        try {
            String sql = "SELECT id_admin FROM admin ORDER BY id_admin DESC LIMIT 1";
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery(sql);

            if (rs.next()) {
                String id = rs.getString("id_admin").substring(1);
                int nomor = Integer.parseInt(id) + 1;

                String nol = "";
                if (nomor < 10) {
                    nol = "0";
                }

                id_admin.setText("A" + nol + nomor);
            } else {
                id_admin.setText("A01");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "ID Admin Otomatis Gagal: " + e);
        }
    }

