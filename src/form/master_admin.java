    private void bsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bsimpanActionPerformed

        if (id_admin.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "ID Admin belum diisi");
            id_admin.requestFocus();
            return;
        }

        if (password.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Password belum diisi");
            password.requestFocus();
            return;
        }

        if (nama_lengkap.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Nama lengkap belum diisi");
            nama_lengkap.requestFocus();
            return;
        }

        String sql = "INSERT INTO admin (id_admin, password, nama_lengkap) VALUES (?, ?, ?)";

        try {
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setString(1, id_admin.getText());
            stat.setString(2, password.getText());
            stat.setString(3, nama_lengkap.getText());

            stat.executeUpdate();

            JOptionPane.showMessageDialog(null, "Data admin berhasil disimpan");
            kosong();
            autonumber();
            aktif();
            datatable();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Data admin gagal disimpan: " + e);
        }
    }//GEN-LAST:event_bsimpanActionPerformed

    private void bubahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bubahActionPerformed
        if (id_admin.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Pilih data admin yang ingin diubah terlebih dahulu");
            return;
        }

        if (password.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Password belum diisi");
            password.requestFocus();
            return;
        }

        if (nama_lengkap.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Nama lengkap belum diisi");
            nama_lengkap.requestFocus();
            return;
        }

        String idDipilih = id_admin.getText().trim();
        String idLogin = UserID.getidadmin();

        String sql = "UPDATE admin SET password=?, nama_lengkap=? WHERE id_admin=?";

        try {
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setString(1, password.getText());
            stat.setString(2, nama_lengkap.getText());
            stat.setString(3, idDipilih);

            stat.executeUpdate();

            JOptionPane.showMessageDialog(null, "Data admin berhasil diubah");

            if (idDipilih.equals(idLogin)) {
                form.menu_utama menuUtama = form.menu_utama.getInstance();

                if (menuUtama != null) {
                    menuUtama.refreshNamaAdmin();
                }
            }

            kosong();
            autonumber();
            aktif();
            datatable();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Data admin gagal diubah: " + e);
        }
    }//GEN-LAST:event_bubahActionPerformed

    private void bhapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bhapusActionPerformed

        if (id_admin.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Pilih data admin yang ingin dihapus terlebih dahulu");
            return;
        }

        String idDipilih = id_admin.getText().trim();
        String idLogin = UserID.getidadmin();

        if (idDipilih.equals(idLogin)) {
            JOptionPane.showMessageDialog(
                    null,
                    "Akun ini sedang digunakan untuk login, sehingga tidak boleh dihapus.",
                    "Peringatan",
                    JOptionPane.WARNING_MESSAGE
            );
            return;
        }

        int ok = JOptionPane.showConfirmDialog(
                null,
                "Apakah Anda yakin ingin menghapus data admin ini?",
                "Konfirmasi Hapus",
                JOptionPane.YES_NO_OPTION
        );

        if (ok == JOptionPane.YES_OPTION) {
            String sql = "DELETE FROM admin WHERE id_admin=?";

            try {
                PreparedStatement stat = conn.prepareStatement(sql);
                stat.setString(1, idDipilih);

                stat.executeUpdate();

                JOptionPane.showMessageDialog(null, "Data admin berhasil dihapus");
                kosong();
                autonumber();
                aktif();
                datatable();

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Data admin gagal dihapus: " + e);
            }
        }
    }//GEN-LAST:event_bhapusActionPerformed

